import turtle as t
t.speed(100)
t.left(90)
l = 15
for i in range(3):
    t.forward(5*l)
    t.right(90)
    t.forward(12*l)
    t.right(90)
t.up()
t.forward(3*l)
t.right(90)
t.forward(2*l)
t.right(90)
t.down()
for i in range(4):
    t.forward(5*l)
    t.right(90)
    t.forward(12*l)
t.up()
for x in range(10,15):
    for y in range(10):
        t.goto(x*l,y*l)
        t.dot(4)
t.done()